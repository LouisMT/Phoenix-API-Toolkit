  defp to_list(list),
    do: Enum.reduce(list, "", fn e, acc -> acc <> "* `#{e}`\n" end) |> String.trim_trailing("\n")

  defp to_table(keyword) do
    Enum.reduce(keyword, "", fn {k, v}, acc -> acc <> "`#{k}` | `#{v}`\n" end)
    |> String.trim_trailing("\n")
  end
